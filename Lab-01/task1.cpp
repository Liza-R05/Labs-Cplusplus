#define _USE_MATH_DEFINES
#include <iostream>   // для ввода-вывода (cin, cout)
#include <cmath>      // для математических функций (sin, cos, sqrt, M_PI)

using namespace std; // чтобы не писать std:: перед каждой функцией   std:: — это пространство имён 

int main() {
    setlocale(LC_ALL, "RUS");
    double a, b;
    double result;
    double n;  // числитель sin(п + b^2)
    double p;  // выражение в скобках знаменателя sin(a) + b^2 * cos(7.2)
    double d;  // знаменатель a^2 * (sin(a) + b^2 * cos(7.2))

    cout << "Введите значение a (например, 0.5236): ";
    cin >> a;
    cout << "Введите значение b (например, 1.7725): ";
    cin >> b;

    n = sin(M_PI + b * b);
    p = sin(a) + b * b * cos(7.2);
    d = a * a * p;

    if (d <= 0) {
        cout << "Ошибка: знаменатель меньше или равен нулю!\n";
        return 1;
    }
    if (n < 0) {
        cout << "Ошибка: числитель отрицательный!\n";
        return 1;
    }

    result = sqrt(n / d);

    cout << "\nСинус от суммы чисел П и квадрата b равен = " << n << "\n";
    cout << "Сумма синуса a и произведения квадрата b на косинус 7.2 равна = " << p << "\n";
    cout << "Знаменатель, равный произведению квадрата a на сумму синуса a и произведения квадрата b на косинус 7.2, равен = " << d << "\n\n";
    cout << "Корень из дроби, равной отношению числителя к знаменателю, равен = " << result << "\n";

    return 0;
}